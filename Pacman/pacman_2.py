    while True:
        # Calculate the rules
        pacman.calculate_rules()

        # Paint the screen
        screen.fill(BLACK)
        scenario.paint(screen)
        pacman.paint(screen)
        pygame.display.update()
        pygame.time.delay(100)

        # Capturate the events
        events = pygame.event.get()
        for e in events:
            if e.type == pygame.QUIT:
                exit()
        pacman.process_events(events)
