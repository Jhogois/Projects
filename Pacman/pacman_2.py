class Pacman:

    def __init__(self) -> None:
        self.column = 1
        self.line = 1
        self.x_center = 400
        self.y_center = 300
        self.size = 800 // 30
        self.x_speed = 0
        self.y_speed = 0
        self.ray = self.size // 2

    def calculate_rules(self):
        self.column = self.column + self.x_speed
        self.line = self.line + self.y_speed
        self.x_center = int(self.column * self.size + self.ray)
        self.y_center = int(self.line * self.size + self.ray)

    def paint(self, screen):
        # Draw the Pacman body
        pygame.draw.circle(
            screen, YELLOW, (self.x_center, self.y_center), self.ray
            )
        # Mouth drawing
        mouth_corner = (self.x_center, self.y_center)
        upper_lip = (self.x_center + self.ray, self.y_center - self.ray)
        lower_lip = (self.x_center + self.ray, self.y_center)
        points = [mouth_corner, upper_lip, lower_lip]
        pygame.draw.polygon(screen, BLACK, points, 0)

        # Pacman's eye
        eye_x = int(self.x_center + self.ray / 3)
        eye_y = int(self.y_center - self.ray * 0.70)
        eye_ray = int(self.ray / 10)
        pygame.draw.circle(screen, BLACK, (eye_x, eye_y), eye_ray, 0)

    def process_events(self, events):
        for e in events:
            if e.type == pygame.KEYDOWN:
                if e.key == pygame.K_RIGHT:
                    self.x_speed = SPEED
                elif e.key == pygame.K_LEFT:
                    self.x_speed = -SPEED
                elif e.key == pygame.K_UP:
                    self.y_speed = -SPEED
                elif e.key == pygame.K_DOWN:
                    self.y_speed = SPEED
            elif e.type == pygame.KEYUP:
                if e.key == pygame.K_RIGHT:
                    self.x_speed = 0
                elif e.key == pygame.K_LEFT:
                    self.x_speed = 0
                elif e.key == pygame.K_UP:
                    self.y_speed = 0
                elif e.key == pygame.K_DOWN:
                    self.y_speed = 0


